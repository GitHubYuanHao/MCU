/*------------------------------------------------
10ms延时函数
------------------------------------------------*/
void delay10ms(unsigned char t)
{
   unsigned char i,j,k;
   for(i=0;i<t;i++)
      for(j=0;j<40;j++)
	     for(k=0;k<75;k++);
}